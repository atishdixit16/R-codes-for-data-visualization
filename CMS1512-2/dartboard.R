dartboard_pi <- function(n)
{
x<-runif(n,-1,1)
y<-runif(n,-1,1)
nc <- length(which(x^2+y^2 <= 1))
pi_est=(nc/n)*4
return(pi_est)
}
values<-NULL
for(i in 1:100) values[i]=dartboard_pi(100)
for(i in 101:200) values[i]=dartboard_pi(1000)
for(i in 201:300) values[i]=dartboard_pi(10000)
for(i in 301:400) values[i]=dartboard_pi(100000)
for(i in 401:500) values[i]=dartboard_pi(1000000)
allData<-matrix(values,nrow=100,ncol=5)
colnames(allData)<-c("10^2","10^3","10^4","10^5","10^6")
pdf("Histrograms.pdf")
par(mfrow=c(2,3))
hist(values[1:100],breaks="FD",main="For Number of Darts = 10^2",xlab="Estimated values of 'pi'")
hist(values[101:200],breaks="FD",main="For Number of Darts = 10^3",xlab="Estimated values of 'pi'")
hist(values[201:300],breaks="FD",main="For Number of Darts = 10^4",xlab="Estimated values of 'pi'")
hist(values[301:400],breaks="FD",main="For Number of Darts = 10^5",xlab="Estimated values of 'pi'")
hist(values[401:500],breaks="FD",main="For Number of Darts = 10^6",xlab="Estimated values of 'pi'")
dev.off()
pdf("Boxplot.pdf")
par(mfrow=c(1,1))
boxplot(allData,main="Boxplot for All Values",xlab="No. of Darts Used for Estimate",ylab="Estimated values of 'pi'")
abline(h=3.1416)
dev.off()
